      <div class="col-xs-12 col-xs-offset-0 col-sm-10 col-sm-offset-1">
        The story begins in 1988. Bent Holter - student at the Technical University in Trondheim (NTNU) decided to do a thesis under the original design of the amplifier transistor that would cure the problems of classical systems.
