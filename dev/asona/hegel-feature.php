      <div id="hegel-more"class="col-xs-12 col-xs-offset-0 col-sm-10 col-sm-offset-1 collapse spacer-10">
        Enemy number one - harmonic distortion. Holter somehow could not accept the fact that when we give a simple signal to the amplifier, you will get that signal plus something else - distortion produced by electronics. Fighting distortion usually meant the deterioration of other parameters, including damping factor. Holter decided to break out the classic schemes and developed a project that became the basis of what we know today as SoundEngine Technology. And where the name of the company came from? Well, after hours Bent played in a rock-metal band called The Hegel Band. When they needed amplifiers for concerts, Holter suggested building them himself. His ideas took on real shape, but it took lots of money to refine the technology. Help came from Telenor - one of the biggest players in the telecommunications industry. The early nineties were a period of prosperity for the brand. First they ensured the patented solution known today as SoundEngine, later developed a DAC (1994) and the first CD Player (1996). Over the next eight years the company has steadily improved the product range and generated more and more profits from year to year. When the financial crisis came, most companies immediately took a defensive strategy which meant cutting costs. Norwegian designer played differently. He hired new engineers to design devices. Sometime later, brand new D/A converters were born. Today Hegel is one of the best established brands in Hi-Fi market, producing integrated, pre and power amplifiers as well as CD players and some of the most modern and sophisticated C/A converters. The SoundEngine Technology was just a start. Today there are six more original technical solutions in Hegel's portfolio. The company has dealers in 32 countries and its products are sold all over the world. Awards and press reviews speak for themselves
