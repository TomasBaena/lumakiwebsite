        Founded by Dr. Fang Bian in late 2005 while he was living in New York City, Bian started Head-Direct, both a web-store and Head-Fi sponsor. He began use of the HiFiMAN brand since 2007. In 2010, he started two small factories in China and moved the headquarters to Tianjin, China in 2011. HiFiMAN designs and manufactures high performance headphones and portable audio products like the HE-6, HE-500, HE-400 and RE-400. In 2009, HiFiMAN released the HM-801, the first true high-end portable music player. HiFiMAN is also known for their Planar-Magnetic headphones that feature advanced driver technology. The company also designs and manufactures in-ear drivers, which have also garnered awards.
