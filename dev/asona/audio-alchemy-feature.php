      <div class="feature-brand-name">Audio Alchemy</div><br>
      <div class="col-xs-12 col-xs-offset-0 col-sm-10 col-sm-offset-1">In the late 1980s and early ’90s, the audio industry was rocked by a sudden shift in medium that left the once mighty tape format obsolete. A small team of engineers, led by renowned sound engineer Peter Madnick, set out to unlock the full potential of digital, and thus, Audio Alchemy was born.
      </div>
      <div class="row spacer-10 center-align">
