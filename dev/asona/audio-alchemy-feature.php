      <div id="audio-alchemy-more"class="col-xs-12 col-xs-offset-0 col-sm-10 col-sm-offset-1 collapse spacer-10">
        As Chief Engineer, Madnick assembled a design and engineering team that would, in just four short years, produce over 100,000 of the company’s DACs, jitter filters, headphone amps, preamps, power amps and phono preamps - many of which are still in use today. While the brand didn’t live beyond the 90’s, its technologies for jitter control, resolution enhancement, and high-resolution analog filtering are still the solutions of choice. The original team, led by Madnick, went on to produce hundreds of innovative products for other brands, including, Pangea, Fender, Counterpoint, Threshold, Motorola, Constellation Audio, Faroudja, Runco, Monster Cable, Belkin and THXEndFragment, with many of these products achieving great notoriety in the audio/video consumer products and industrial industries. However, Madnick and his team could never let go of what they built at Audio Alchemy, it was something special – the right product at the right price, at just the right time. With the original alchemists still in contact and sound quality at arguably an all-time low, an opportunity in 2013 to revive the brand presented itself. Madnick jumped on it, pulling together many of the original team members. Madnick and the team has maintained the brand’s original ethos of superior technology and value, layering in their experience over the years to bring a level of elegance and high-end performance they could only once dream of. As Madnick puts it, “Since the days of the original Audio Alchemy, you’ve grown up and so have we.”
